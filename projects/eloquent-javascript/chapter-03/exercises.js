function isEven(num) {
//what if the number passed in is a negative number?
if(num < 0){
  num = Math.abs(num);
}
if(num === 0){
  return true;
}
if (num === 1){
  return false;
}//in all other cases apply isEven to n - 2
return isEven(num - 2);

}

